CREATE TABLE Shop (
  id_Shop INTEGER UNSIGNED  NOT NULL   AUTO_INCREMENT,
  productName VARCHAR(1000)  NULL  ,
  workerName VARCHAR(1000)  NULL  ,
  omount INTEGER UNSIGNED  NULL  ,
  productPrice DOUBLE  NULL  ,
  supplierName VARCHAR(1000)  NULL  ,
  goodsCategory VARCHAR(1000)  NULL    ,
PRIMARY KEY(id_Shop));




